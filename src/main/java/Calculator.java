public class Calculator {

    public Calculator() {

    }

    public int add(int numOne, int numTwo){
        return numOne + numTwo;
    }

    public int subtract(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i * i1;
    }

    public double divide(double i, double i1) {
        return i / i1;
    }
}
